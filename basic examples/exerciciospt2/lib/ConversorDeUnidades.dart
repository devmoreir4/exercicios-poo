class ConversorDeUnidades {

  static double kmToMilhas(double km){
    return (km * 0.621371);
  }

  static double milhasToKm(double milhas){
    return (milhas / 0.621371);
  }

  static double kgToLibras(double kg) {
    return kg * 2.20462;
  }

  static double hourToMinute(double hora) {
    return (hora * 60);
  }

  static double segToMinute(double seg) {
    return (seg / 60);
  }

}